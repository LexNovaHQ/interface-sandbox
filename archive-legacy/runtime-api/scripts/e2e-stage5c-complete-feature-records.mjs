import assert from "node:assert/strict";
import { buildStage5CanonicalInput } from "../src/diligence/stage5/stage5.runtime.js";
import { runStage5A } from "../src/diligence/stage5/5a/5a.runtime.js";
import { runStage5B } from "../src/diligence/stage5/5b/5b.runtime.js";
import { runStage5C } from "../src/diligence/stage5/5c/5c.runtime.js";
import { STAGE5C_OUTPUT_VERSION } from "../src/diligence/stage5/5c/5c.dictionary.js";
import { fixtureCompanyProfile, fixtureStage5Input, assertWindowIntegrity } from "./e2e-stage5-full-runtime.mjs";

const canonicalInput = buildStage5CanonicalInput({ companyProfile: fixtureCompanyProfile, stage5Input: fixtureStage5Input });
const stage5a = await runStage5A({ canonicalInput });
const stage5b = await runStage5B({ canonicalInput, stage5a });
const stage5c = await runStage5C({ canonicalInput, stage5a, stage5b });

assert.equal(stage5c.ok, true);
assert.equal(stage5c.stage5c_output_version, STAGE5C_OUTPUT_VERSION);
assert.equal(stage5c.complete_feature_records.length, stage5a.admitted_functions.length);
assert.equal(stage5c.validation.merged_old_5c_and_old_5d, true);
assert.equal(stage5c.validation.metadata_used_as_primary_source, false);
assert.equal(stage5c.validation.index_used_as_primary_source, false);
assert.ok(stage5c.supplemental_evidence_windows.length >= stage5a.admitted_functions.length);
assert.ok(stage5c.supplemental_evidence_windows.every((window) => window.window_type === "DATA_MECHANICS_WINDOW"));
assert.ok(stage5c.supplemental_evidence_windows.every((window) => window.verbatim_data_mechanics_text === window.verbatim_text));
assert.ok(stage5c.supplemental_evidence_windows.every((window) => window.parent_window_id));
assert.ok(stage5c.supplemental_evidence_windows.every((window) => window.used_for.includes("data_touchpoint_extraction")));
assert.ok(stage5c.complete_feature_records.every((row) => row.evidence_window_refs.length));
assert.ok(stage5c.complete_feature_records.every((row) => row.inherited_feature_window_refs.length));
assert.ok(stage5c.complete_feature_records.every((row) => row.supplemental_tag_window_refs.length));
assert.ok(stage5c.complete_feature_records.every((row) => row.supplemental_data_window_refs.length));
assert.ok(stage5c.complete_feature_records.every((row) => row.data_touchpoints.length > 0));
assert.ok(stage5c.complete_feature_records.every((row) => row.data_provenance.length > 0));
assert.ok(stage5c.complete_feature_records.every((row) => row.data_touchpoints.every((tp) => tp.source_window_refs.length)));
assert.ok(stage5c.complete_feature_records.every((row) => row.data_provenance.every((dp) => dp.source_window_refs.length)));
assert.ok(stage5c.complete_feature_records.every((row) => row.metadata_used_as_primary_source === false));
assert.ok(stage5c.complete_feature_records.every((row) => row.index_used_as_primary_source === false));
assert.ok(stage5c.data_provenance_seeds.length >= stage5c.complete_feature_records.length);
assert.ok(stage5c.regulated_surface_seeds.length >= stage5c.complete_feature_records.length);
assert.ok(stage5c.vault_question_seeds.length >= stage5c.complete_feature_records.length);
assertWindowIntegrity(canonicalInput, stage5a.feature_evidence_windows);
assertWindowIntegrity(canonicalInput, stage5b.supplemental_evidence_windows);
assertWindowIntegrity(canonicalInput, stage5c.supplemental_evidence_windows);
console.log("e2e:stage5c:complete-feature-records passed");
