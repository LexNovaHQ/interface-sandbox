function sampleSection(section_id, section_title) { return { section_id, artifact_name: `normalized_section__${section_id}`, section_title, section_order: 1, section_status: "LOCKED", reviewer_summary: "Summary", section_limitations: ["FORBIDDEN SECTION LIMITATION"], source_artifacts_used: ["target_profile"], normalization: { internal: true }, vault_mapping: { internal: true }, subsections: [subsection(`${section_id}_subsection`, "Sample", "Target", { public_name: "Example", source_path: "target_profile.target_identity.brand_name", technical_refs: { evidence_id: "E-001" }, row_type: "FORBIDDEN", technical_annexure_only: false })] }; }
function subsection(subsection_id, subsection_title, label, value) { return { subsection_id, subsection_title, fields: [{ field_id: `${subsection_id}_field`, label, value, source_artifact: "target_profile", source_path: "target_profile.target_identity.brand_name", technical_refs: { evidence_id: "E-002" }, qualified_review_note: "Verify before reliance.", limitation: "" }] }; }
function sectionTitle(sectionId) { return sectionId.replace(/_/g, " ").replace(/\b\w/g, (letter) => letter.toUpperCase()); }
